package org.b3ds.fhir.patient.mockdata;

import org.springframework.stereotype.Component;

@Component
public class Person {

	private String id;
	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
	
}